import java.util.*;

public class Perimeter_of_parallelogram_10 {
    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);
        // Perimeter Of Parallelogram  P = 2(a+b)
        int a = input.nextInt();
        int b = input.nextInt();
        System.out.println("Perimeter Of Parallelogram : " + 2*(a + b));
    }
}