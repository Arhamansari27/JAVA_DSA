import java.util.*;

public class Perimeter_of_rectangle_11 {
    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);
        // Perimeter Of Rectangle P = 2(l+w)
        int l = input.nextInt();
        int w = input.nextInt();
        System.out.println("Perimeter Of Rectangle is " + 2 * (l + w));
    }
}
